<h3>Converting to Harrison's</h3>
<ul>
    <li>Consult with an avian veterinarian before any diet change and annually thereafter.</li>
    <li>Mix Harrison's with your bird's current diet and note acceptance.</li>
    <li>If your bird needs more time, taper amount given over four weeks, increasing Harrison's by 25% each week (reducing other food by 25%) until you can safely remove the previous food altogether.</li>
    <li>If needed, scatter Harrison'snuggets over white paper or a mirror on the floor of the enclosure.</li>
</ul>

