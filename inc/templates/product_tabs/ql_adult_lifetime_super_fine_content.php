<h3>Storage and Shelf Life</h3>
<p>Harrison's is committed to protecting your bird from artificial preservatives. Here are some suggestions to keep your food fresh without preservatives:</p>
<ul>
    <li>Squeeze all air out of the bag and zip it shut at the top.</li>
    <li>If the zip lock gets removed or damaged, fold the top over several times and close with a clip.</li>
    <li>Keep food in original bag. Do not repackage into plastic bags or containers.</li>
    <li>Use contents within 8 weeks of opening bag.</li>
    <li>Purchase Harrison's foods only in their original packaging.</li>
    <li>Refrigeration or freezing after opening may help maintain freshness.</li>
</ul>

